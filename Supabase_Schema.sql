CREATE POLICY "Insert post"
ON public.discussion_posts FOR INSERT TO authenticated
WITH CHECK (
  user_id = auth.uid() AND
  EXISTS (
    SELECT 1 FROM public.users
    WHERE id = auth.uid() AND
    public.users.department = discussion_posts.department AND
    public.users.section = discussion_posts.section
  )
);

-- 3. Delete/Modify own post or admin
CREATE POLICY "Modify or delete own post"
ON public.discussion_posts FOR ALL TO authenticated
USING (
  user_id = auth.uid() OR
  EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'super_admin')
);

