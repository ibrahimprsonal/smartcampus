-- -------------------------------------------------------------
-- USERS Policies
-- -------------------------------------------------------------
-- 1. Anyone authenticated can read other users profiles (to enable messaging/discussion directory listing)
CREATE POLICY "Allow authenticated users to read user profiles"
ON public.users FOR SELECT TO authenticated
USING (true);

-- 2. Users can update their own profile
CREATE POLICY "Allow users to update their own profile"
ON public.users FOR UPDATE TO authenticated
USING (auth.uid() = id)
WITH CHECK (auth.uid() = id);

-- 3. Function to safely get user role without infinite recursion
CREATE OR REPLACE FUNCTION public.get_user_role()
RETURNS public.user_role
LANGUAGE sql
SECURITY DEFINER SET search_path = public
AS $$
  SELECT role FROM public.users WHERE id = auth.uid();
$$;

-- 4. Super admins can manage everything in the users table
CREATE POLICY "Allow admins full control over users"
ON public.users TO authenticated
USING (public.get_user_role() = 'super_admin');

-- -------------------------------------------------------------
-- DEPARTMENTS Policies
-- -------------------------------------------------------------
-- 1. Anyone authenticated can read departments
CREATE POLICY "Allow reading departments"
ON public.departments FOR SELECT TO authenticated USING (true);

-- 2. Only admins can insert/update/delete
CREATE POLICY "Admin control departments"
ON public.departments FOR ALL TO authenticated
USING (EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'super_admin'));

-- -------------------------------------------------------------
-- ACTIVITY LOG Policies
-- -------------------------------------------------------------
-- 1. Users can read their own activity logs
CREATE POLICY "Read own activity log"
ON public.activity_log FOR SELECT TO authenticated
USING (user_id = auth.uid());

-- 2. System/Admins can see everything
CREATE POLICY "Admin view activity log"
ON public.activity_log FOR SELECT TO authenticated
USING (EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'super_admin'));

-- -------------------------------------------------------------
-- CONVERSATION REQUESTS Policies
-- -------------------------------------------------------------
-- 1. View if participating
CREATE POLICY "View participating conversations"
ON public.conversation_requests FOR SELECT TO authenticated
USING (user_a = auth.uid() OR user_b = auth.uid());

-- 2. Create requests
CREATE POLICY "Create conversation request"
ON public.conversation_requests FOR INSERT TO authenticated
WITH CHECK (initiated_by = auth.uid());

-- 3. Update requests (if accepting/rejecting)
CREATE POLICY "Update conversation request"
ON public.conversation_requests FOR UPDATE TO authenticated
USING (user_a = auth.uid() OR user_b = auth.uid());

-- -------------------------------------------------------------
-- CR/ACR CHAT Policies
-- -------------------------------------------------------------
-- 1. Access restricted to CR, ACR, and Super Admins
CREATE POLICY "CR ACR Chat Access"
ON public.cr_acr_chat TO authenticated
USING (
  EXISTS (
    SELECT 1 FROM public.users
    WHERE id = auth.uid() AND (role IN ('cr', 'acr', 'super_admin'))
  )
);

-- -------------------------------------------------------------
-- DEADLINES Policies
-- -------------------------------------------------------------
-- 1. Read deadlines if directed at your group, or created by you, or admin
CREATE POLICY "Select visible deadlines"
ON public.deadlines FOR SELECT TO authenticated
USING (
  created_by = auth.uid() OR
  EXISTS (
    SELECT 1 FROM public.users
    WHERE id = auth.uid() AND (
      role = 'super_admin' OR
      (
        (target_department IS NULL OR target_department = public.users.department) AND
        (target_semester IS NULL OR target_semester = public.users.semester) AND
        (target_section IS NULL OR target_section = public.users.section)
      )
    )
  )
);

-- 2. Create deadlines (only teachers, cr, acr, admin)
CREATE POLICY "Create deadlines"
ON public.deadlines FOR INSERT TO authenticated
WITH CHECK (
  EXISTS (
    SELECT 1 FROM public.users
    WHERE id = auth.uid() AND (role IN ('cr', 'acr', 'teacher', 'super_admin'))
  )
);

-- 3. Modify own deadline or admin modify
CREATE POLICY "Modify deadlines"
ON public.deadlines FOR UPDATE TO authenticated
USING (
  created_by = auth.uid() OR
  EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'super_admin')
);

-- 4. Delete own deadline or admin delete
CREATE POLICY "Delete deadlines"
ON public.deadlines FOR DELETE TO authenticated
USING (
  created_by = auth.uid() OR
  EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'super_admin')
);

-- -------------------------------------------------------------
-- DEADLINE LINKS Policies
-- -------------------------------------------------------------
-- Inherits visibility logic by checking if the parent deadline exists in accessible deadlines
CREATE POLICY "Select deadline links"
ON public.deadline_links FOR SELECT TO authenticated
USING (EXISTS (SELECT 1 FROM public.deadlines d WHERE d.id = deadline_links.deadline_id));

-- Creators/Admins can manage
CREATE POLICY "Manage deadline links"
ON public.deadline_links FOR ALL TO authenticated
USING (
  EXISTS (
    SELECT 1 FROM public.deadlines d
    WHERE d.id = deadline_links.deadline_id AND (
      d.created_by = auth.uid() OR
      EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'super_admin')
    )
  )
);

-- -------------------------------------------------------------
-- DEADLINE READS Policies
-- -------------------------------------------------------------
-- 1. Read own records
CREATE POLICY "Read own deadline acknowledges"
ON public.deadline_reads FOR SELECT TO authenticated
USING (user_id = auth.uid());

-- 2. Deadline creator can view who acknowledged it
CREATE POLICY "Creator view deadline acknowledges"
ON public.deadline_reads FOR SELECT TO authenticated
USING (
  EXISTS (
    SELECT 1 FROM public.deadlines d
    WHERE d.id = deadline_reads.deadline_id AND d.created_by = auth.uid()
  )
);

-- 3. Create / Update acknowledgment for oneself
CREATE POLICY "Manage own deadline acknowledgment"
ON public.deadline_reads FOR ALL TO authenticated
USING (user_id = auth.uid())
WITH CHECK (user_id = auth.uid());

-- -------------------------------------------------------------
-- DISCUSSION POSTS Policies
-- -------------------------------------------------------------
-- 1. Read posts if in the same department/section or if admin
CREATE POLICY "Select visible posts"
ON public.discussion_posts FOR SELECT TO authenticated
USING (
  EXISTS (
    SELECT 1 FROM public.users
    WHERE id = auth.uid() AND (
      role = 'super_admin' OR
      (
        public.users.department = discussion_posts.department AND
        public.users.section = discussion_posts.section
      )
    )
  )
);

