-- =============================================================
-- 1. CREATE CUSTOM TYPES (ENUMS)
-- =============================================================
CREATE TYPE public.user_role AS ENUM ('student', 'cr', 'acr', 'teacher', 'super_admin');
CREATE TYPE public.user_status AS ENUM ('pending', 'approved', 'rejected', 'banned');
CREATE TYPE public.request_status AS ENUM ('pending', 'accepted', 'rejected');
CREATE TYPE public.section_request_status AS ENUM ('pending', 'approved', 'rejected');
CREATE TYPE public.priority_level AS ENUM ('normal', 'high', 'urgent');

-- =============================================================
-- 2. HELPER FUNCTIONS
-- =============================================================

-- Automatically update the updated_at timestamp
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ language 'plpgsql';

